DAT EQU R0
ADR EQU R1

ORG 0000h
MOV SP,#2Fh

START:
    ACALL LCD_INIT

LOOP:
    ACALL COLUMN_3
    CJNE A,#00001110b,LOOP

    ACALL LINE1
    ACALL WORD1

WAIT_RELEASE:
    ACALL COLUMN_3
    CJNE A,#00001110b,LOOP
    JMP WAIT_RELEASE

LCD_INIT:
    ; 0x28
    MOV DAT,#020h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    MOV DAT,#080h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY

    ; 0x0E
    MOV DAT,#000h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    MOV DAT,#0E0h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY

    ; 0x01 clear
    MOV DAT,#000h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    MOV DAT,#010h
    MOV ADR,#08h
    ACALL WRIT
    ACALL WLDELAY
    RET

LINE1:
    ; cmd 0x80
    MOV DAT,#080h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    MOV DAT,#000h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    RET

WORD1:
    ; '3' (0x33) x2
    MOV DAT,#031h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    MOV DAT,#031h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY

    MOV DAT,#031h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    MOV DAT,#031h
    MOV ADR,#08h
    ACALL WRIT
    ACALL LDELAY
    RET

COLUMN_3:
    CLR P3.6
    MOV P2,#030h
    MOV A,P0
    ANL A,#00Fh
    RET

WRIT:
    SETB P3.6
    MOV P0,DAT
    MOV P2,ADR
    NOP
    MOV P2,#00h
    RET

LDELAY:
    MOV R5,#020
D1:
    MOV R6,#100
D2:
    DJNZ R6,D2
    DJNZ R5,D1
    RET

WLDELAY:
    MOV R7,#005
WD1:
    ACALL LDELAY
    DJNZ R7,WD1
    RET

END
