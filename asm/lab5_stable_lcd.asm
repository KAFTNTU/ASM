    MOV ADR,#08h
    CALL WRIT
    CALL LDELAY
    MOV DAT,#031h
    MOV ADR,#08h
    CALL WRIT
    CALL LDELAY
    MOV DAT,#0B1h
    MOV ADR,#08h
    CALL WRIT
    CALL LDELAY
