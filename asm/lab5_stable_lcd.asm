    MOV ADR,#08h
    CALL WRIT
    CALL LDELAY
    MOV DAT,#0A0h
    MOV ADR,#08h
    CALL WRIT
    CALL LDELAY
    RET

COLUMN_1:
    CLR P3.6
    MOV P2,#060h
    MOV A,P0
    ANL A,#00Fh
    RET

COLUMN_2:
    CLR P3.6
    MOV P2,#050h
    MOV A,P0
    ANL A,#00Fh
    RET

COLUMN_3:
    CLR P3.6
    MOV P2,#030h
    MOV A,P0
    ANL A,#00Fh
    RET

WAITREL_COL1:
WR1:
    CALL COLUMN_1
    CJNE A,#00Fh,WR1
    RET

WAITREL_COL2:
WR2:
    CALL COLUMN_2
    CJNE A,#00Fh,WR2
    RET

WAITREL_COL3:
WR3:
    CALL COLUMN_3
    CJNE A,#00Fh,WR3
    RET

WRIT:
WRITE:
    SETB P3.6
    MOV P0,DAT
    MOV P2,ADR
    NOP
    MOV P2,#00h
    RET

LDELAY:
    MOV R5,#DL_OUTER
LD1:
    MOV R6,#DL_INNER
LD2:
    DJNZ R6,LD2
    DJNZ R5,LD1
    RET

WLDELAY:
    MOV R7,#WDL_COUNT
WL2:
    ACALL LDELAY
    DJNZ R7,WL2
    RET

END
